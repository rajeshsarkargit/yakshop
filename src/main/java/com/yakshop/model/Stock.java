package com.yakshop.model;

public class Stock {
	private Double milk;
	private Double skins;
	/**
	 * @return the milk
	 */
	public Double getMilk() {
		return milk;
	}
	/**
	 * @param milk the milk to set
	 */
	public void setMilk(Double milk) {
		this.milk = milk;
	}
	/**
	 * @return the skins
	 */
	public Double getSkins() {
		return skins;
	}
	/**
	 * @param skins the skins to set
	 */
	public void setSkins(Double skins) {
		this.skins = skins;
	}
}
